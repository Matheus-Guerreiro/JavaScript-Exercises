const nota = 10;

switch (nota) {
  case 0:
    console.log("Tudo errado!");
    break;

  case 1:
    console.log("Tente outra vez");
    break;

  case 2:
    console.log("Sem coerência");
    break;

  case 3:
    console.log("Não entendi");
    break;

  case 4:
    console.log("Se esforce");
    break;

  case 5:
    console.log("Na média");
    break;

  case 6:
    console.log("Melhore");
    break;

  case 7:
    console.log("Aceitável");
    break;

  case 8:
    console.log("Ótimo");
    break;

  case 9:
    console.log("Parabéns");
    break;

  case 10:
    console.log("Perfeito!");
    break;

  default:
      console.log("Não é uma nota válido")
    break;
}
